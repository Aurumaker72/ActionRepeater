namespace ActionRepeater.Core.Input;

public enum MouseButton
{
    /// <summary>
    /// The left button.
    /// </summary>
    Left,
    /// <summary>
    /// The middle button (wheel button)
    /// </summary>
    Middle,
    /// <summary>
    /// The right button.
    /// </summary>
    Right,
    /// <summary>
    /// The first X button (extra mouse [usually] side button)
    /// </summary>
    X1,
    /// <summary>
    /// The second X button (extra mouse [usually] side button)
    /// </summary>
    X2
}
